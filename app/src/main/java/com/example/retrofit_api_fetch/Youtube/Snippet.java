
package com.example.retrofit_api_fetch.Youtube;


import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;


public class Snippet {

    @SerializedName("publishedAt")
    @Expose
    private String publishedAt;
    @SerializedName("channelId")
    @Expose
    private String channelId;
    @SerializedName("title")
    @Expose
    private String title;
    @SerializedName("description")
    @Expose
    private String description;
    @SerializedName("isDefaultStream")
    @Expose
    private Boolean isDefaultStream;

    public String getPublishedAt() {
        return publishedAt;
    }

    public void setPublishedAt(String publishedAt) {
        this.publishedAt = publishedAt;
    }

    public String getChannelId() {
        return channelId;
    }

    public void setChannelId(String channelId) {
        this.channelId = channelId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Boolean getIsDefaultStream() {
        return isDefaultStream;
    }

    public void setIsDefaultStream(Boolean isDefaultStream) {
        this.isDefaultStream = isDefaultStream;
    }

}
